// JavaScript identifier naming convention examples

// 1. camelCase - commonly used for variable names
var camelCaseExample = 'helloWorld';

// 2. PascalCase - commonly used for constructor or class-like names
var PascalCaseExample = 'MyClassName';

// 3. snake_case - sometimes used in constants or legacy code
var snake_case_example = 'snake_case';

// 4. kebab-case - not valid in JavaScript identifiers
// var kebab-case-example = 'not valid';

// 5. UPPER_SNAKE_CASE - commonly used for constants
var UPPER_SNAKE_CASE = 100;

// 6. lowerCamelCase - same as camelCase
var lowerCamelCase = 'lowerCamelCase';

// 7. $ and _ prefix examples
var $_prefixed = 'dollarUnderscore';
var _underscorePrefixed = 'underscore';

console.log(camelCaseExample, PascalCaseExample, snake_case_example, UPPER_SNAKE_CASE, lowerCamelCase, $_prefixed, _underscorePrefixed);
